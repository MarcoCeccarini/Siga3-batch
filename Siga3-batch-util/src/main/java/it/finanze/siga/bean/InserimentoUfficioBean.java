package it.finanze.siga.bean;

public class InserimentoUfficioBean {

	private String codiceUfficio;
	private String codiceCdr;
	private String descrizioneUfficio;
	private String descrizioneCdr;
	private String tipologiaUfficio;
	private String tipologiaCdr;
	private String provincia;
	private String regione;
	private String bloccato;
	
	public String getCodiceUfficio() {
		return codiceUfficio;
	}
	public void setCodiceUfficio(String codiceUfficio) {
		this.codiceUfficio = codiceUfficio;
	}
	public String getCodiceCdr() {
		return codiceCdr;
	}
	public void setCodiceCdr(String codiceCdr) {
		this.codiceCdr = codiceCdr;
	}
	public String getTipologiaUfficio() {
		return tipologiaUfficio;
	}
	public void setTipologiaUfficio(String tipologiaUfficio) {
		this.tipologiaUfficio = tipologiaUfficio;
	}
	public String getTipologiaCdr() {
		return tipologiaCdr;
	}
	public void setTipologiaCdr(String tipologiaCdr) {
		this.tipologiaCdr = tipologiaCdr;
	}
	public String getProvincia() {
		return provincia;
	}
	public void setProvincia(String provincia) {
		this.provincia = provincia;
	}
	public String getRegione() {
		return regione;
	}
	public void setRegione(String regione) {
		this.regione = regione;
	}
	public String getDescrizioneUfficio() {
		return descrizioneUfficio;
	}
	public void setDescrizioneUfficio(String descrizioneUfficio) {
		this.descrizioneUfficio = descrizioneUfficio;
	}
	public String getDescrizioneCdr() {
		return descrizioneCdr;
	}
	public void setDescrizioneCdr(String descrizioneCdr) {
		this.descrizioneCdr = descrizioneCdr;
	}
	public String getBloccato() {
		return bloccato;
	}
	public void setBloccato(String bloccato) {
		this.bloccato = bloccato;
	}
	
	
}
