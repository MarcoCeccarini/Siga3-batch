package it.finanze.scheduler.bean;

public class CoppiaCFBean {

	private String cfIniziale;
	private String cfFinale;
	private int dimBlocco;
	
	public String getCfIniziale() {
		return cfIniziale;
	}
	public void setCfIniziale(String cfIniziale) {
		this.cfIniziale = cfIniziale;
	}
	public String getCfFinale() {
		return cfFinale;
	}
	public void setCfFinale(String cfFinale) {
		this.cfFinale = cfFinale;
	}
	public int getDimBlocco() {
		return dimBlocco;
	}
	public void setDimBlocco(int dimBlocco) {
		this.dimBlocco = dimBlocco;
	}
	
	
	
}
